public class Suma {
    
    protected float a, b;
    
    public Suma(float a, float b){
        this.a = a;
        this.b = b;
    }
    
    public float getSumaA(){
        return a;
    }
    
    public void setSumaA(float a){
        this.a = a;
    }
    
    public float getSumaB(){
        return b;
    }
    
    public void setSumaB(float b){
        this.b = b;
    }
    
}
